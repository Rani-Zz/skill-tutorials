#include<iostream>
#include<vector>
#include<algorithm>
using namespace std;
int longest(vector<long long> nums, long long s)
{
	if (nums.empty())
		return -1;
	long long res = -1;
	long long l = 0, r = 0, tmp = nums[0];

	while (r<nums.size()-1)
	{
		if (tmp == s)
		{
			res = max(res, r - l + 1);
		}
		r++;
		tmp += nums[r];
		while (tmp > s&&l<r)
		{
			tmp -= nums[l];
			l++; 
		}
		
	}
	return res;
}
int main()
{
	long long  n, s;
	while (cin >> n >> s)
	{
		vector <long long> nums(n, 0);
		for (int i = 0; i < n; i++)
			cin >> nums[i];
		long long  res = longest(nums, s);
		cout << res << endl;
	}
	return 0;
}
